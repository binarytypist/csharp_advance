namespace InfernoInfinity.Enums
{
    // Represents how rare and powerful a weapon is
    // Used as a multiplier for base weapon damage
    public enum WeaponRarity
    {
        // Weakest weapon type
        Common = 1,

        // Slightly better than common
        Uncommon = 2,

        // Strong weapon
        Rare = 3,

        // Strongest weapon tier
        Epic = 5
    }
}