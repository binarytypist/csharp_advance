namespace Traffic_Lights.Enums
{
    //  An enum (enumeration) is a special type in C# that defines a set of named constant values.
    // This enum represents the possible states of a traffic light
    public enum LightColor
    {
        // Red light → cars must stop
        Red,

        // Green light → cars can go
        Green,

        // Yellow light → warning, prepare to stop
        Yellow
    }
}